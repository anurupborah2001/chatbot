 `publication` varchar(100) DEFAULT NULL,
 `ad_type` varchar(100) DEFAULT NULL,
 `ad_nature` varchar(100) DEFAULT NULL,
 `start_date` date DEFAULT NULL,
 `end_date` date DEFAULT NULL,
 `ad_vehicle_image_name` varchar(100) DEFAULT NULL,
 `ad_property_image_name` varchar(100) DEFAULT NULL,
 `ad_vehicle_qr_code_name` varchar(100) DEFAULT NULL,
