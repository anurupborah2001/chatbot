 `phone_number` varchar(100) NOT NULL,
 `email` varchar(100) NOT NULL,
`bill_to_address_line1` varchar(100) NOT NULL,
`bill_to_address_city` varchar(100) NOT NULL,
`bill_to_address_state` varchar(100) NOT NULL,
`bill_to_address_postal_code` varchar(100) NOT NULL,
