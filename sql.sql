`price` varchar(100) DEFAULT NULL,
`days` varchar(100) DEFAULT NULL,
`sub_total` varchar(100) DEFAULT NULL,
`gst` varchar(100) DEFAULT NULL,
`total` varchar(100) DEFAULT NULL,
